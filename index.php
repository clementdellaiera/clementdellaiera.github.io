<button class="accordion"> <b> Lectures </b></button>
<div class="panel">
    <p> 
    <b> Corina Ciobotaru, An introduction to Bruhat-Tits buildings</b><br>
        <br>
        A building is an important notion in Mathematics which was introduced by Bruhat and Tits around 1970-1980 in order to classify algebraic groups over an arbitrary field. Having at once a combinatorial and geometric structure, a building simultaneously generalizes certain aspects of flag manifolds, finite projective planes, and Riemannian symmetric spaces. They play a central role in the fields of geometric group theory (i.e. the study of Coxeter groups, CAT(0) spaces, etc...), the representation theory of algebraic groups, or discrete mathematics. The aim of this course is to give a fair introduction to the subject via the concrete example of the Bruhat-Tits building of \(SL_n(\mathbb Q_p)\). If time permits, at the end of the lectures we will employ the geometry of the freshly constructed affine building to compute Chabauty limits of various subgroups of \(SL_n(\mathbb Q_p)\). Lecture notes will be provided.<br>
    <br>
        <b>Piotr Nowak, Large scale geometry of group actions</b><br>
        <br>
        In these these lectures I will describe a construction of a metric space out of an action of a finitely generated group on a compact space, the so called warped cones introduced by John Roe. I will then survey some of their coarse geometric properties, connections to box spaces and expanders.<br>
    <br>
    <b>Reading material</b><br>
    <br>
    <a href="docs/Ciobotaru, An introduction to Bruhat-Tits buildings, Lecture notes (ENS_Lyon_2023).pdf">Ciobotaru, An introduction to Bruhat-Tits buildings, Lecture notes (2023)</a><br>
    </p>
</div>        
        
<button class="accordion"> <b> Research talks</b></button> 
<div class="panel">
    <p>
    <b> Naomi Andrew, Centralisers in (outer) automorphism groups</b><br>
        <br>Given a group G, we can ask about its subgroups. Centralisers of elements are easy to define, but maybe not so easy to understand: even in such well studied groups as \(Out(\mathbb F_n)\) they are not yet understood in general. I'll discuss work with Armando Martino where we extend what is known in \(Out(\mathbb F_n)\), involving a (surprising?) connection to free-by-cyclic groups and their automorphisms as well as working with actions on trees. The strategies seem like they should apply in many more cases, and as time allows I'll discuss ongoing work (with Gilbert Levitt and Armando Martino) exploring these possibilities.<br>
        <br>
    <b> Kevin Boucher, Uniformly bounded representations of hyperbolic groups</b><br>
        <br>
        After an introduction to the subject of boundary representations of hyperbolic groups, I will present some recent development motivated by a spectral formulation of the so-called Shalom conjecture.<br>
        <br>
        This a joint work with Dr Jan Spakula.<br>
        <br>
        Slides <a href="docs/Boucher,%20Boundary%20representations%20of%20hyperbolic%20groups%20(slides%20CGD%20ENS%20Lyon%2031_08_2023).pdf">here</a>.<br>
        <br>
        
    <b>Amandine Escalier, Quantitative measure equivalence and graph products</b><br>    
        <br>
        Measure equivalence was introduced by Gromov in the 90’s. Although presented as a measure theoretical analogue of quasi-isometry, measure equivalence sometimes crushes two groups with very different geometries to a same equivalence class. Recently, Delabie, Koivisto, Le Maître and Tessera introduced a quantitative version of measure equivalence, which aims to specify how close the two equivalent actions are.<br>
        <br>
        In this talk we will focus on the behavior of graph products under quantitative measure equivalence. Through this study, we will highlight the links between the latter notion and geometric properties. This is joint work with Camille Horbez.<br>
    <br>  
        Slides <a href="docs/Escalier,%20Quantitative%20measure%20equivalence%20and%20graph%20products%20(slides%20CGD%20ENS%20Lyon%202023_09).pdf">here</a>.<br>
        <br>
    <b>Uwe Franz, Gaussian parts of compact quantum groups</b><br>
        <br>
        Compact quantum groups, as introduced by Woronowicz in the 1980's, are unital C*-algebras with additional structure, which generalise the algebra of continuous functions on a compact group. Many notions and results from the theory of compact groups, like the Haar measure, their Peter-Weyl representation theory, and Tannaka-Krein duality, extend to this more general framework. In this talk I will propose a definition of connectedness and the connected component of the unit for compact quantum groups, motivated by Schürmann definition of Gaussianity for Lévy processes on involutive bialgebras. We will see that for commutative compact quantum groups these definitions are equivalent to the classical definitions. Next we will consider new features that arise in the noncommutative case, and finally we will determine the `connected component of the unit' (which we dub `Gaussian part') for several examples of interest. The talk will conclude with several related open problems.<br>
        <br>
    <b>Sam Hughes, Dynamics and finite quotients of free-by-cyclic groups</b><br>    
    <br>
        In this talk we will investigate to what extent we can distinguish two free-by-cyclic groups by their finite quotients. I will present recent work joint with Monika Kudlinska proving that there exist at most finitely many free-by-cyclic groups with the same finite quotients as a generic free-by-cyclic group.  The talk will focus on explaining how we encode some of the dynamics of a free-by-cyclic group into its finite quotients.<br>
    <br>
    <b> François Le Maître, On quantitative orbit equivalence for actions of the group of integers</b><br>
        <br>
        Dye's theorem informs us that up to conjugacy, all probability measure-preserving ergodic actions of countable infinite amenable groups share the same orbits, so that the theory of orbit equivalence trivializes for such groups.  Quantitative orbit equivalence aims at remembering more information than just the orbits, notably by putting additional conditions on the cocycles one obtains once two actions share the same orbits. In this talk, I will try to give an overview of  what is known for actions of the easiest countable infinite amenable group, namely the group of integers.<br>
        <br>
        <b> Samantha Pilgrim, The Dynamic Asymptotic Dimension and Coarse Geometry</b><br>
        <br>We will give an introduction to the dynamic asymptotic dimension of Guentner, Willett, and Yu and describe some connections with geometry.  This dimension theory for group actions appears as though it should be sensitive to the topology of the space being acted on (the definition involves open sets) but we will see it often coincides with the asymptotic dimension of the acting group.  Our approach takes inspiration from coarse geometry, especially the study of box spaces.<br>
        <br>
        Slides <a href="docs/Pilgrim,%20DAD%20and%20geometry%20(slides%20CGD%20ENS%20Lyon%202023).pdf">here</a>.<br>
        <br>
    <b> Antoine Poulin, Quasi-treeable equivalence relations</b><br>
        <br>
         It is known that free actions of virtually free groups are treeable. We prove an analoguous results for locally finite Borel graphs G: if every G component is quasi-isometric to a tree, then the equivalence relation is treeable, under the conditions that the G-components be one-ended or have bounded degree.<br> 
        <br>
        Slides <a href="docs/Poulin,%20Quasi_treeable_equivalence_relations%20(slides%20CGD%20ENS%20Lyon).pdf">here</a>.
        <br>
    </p>
</div>
    
